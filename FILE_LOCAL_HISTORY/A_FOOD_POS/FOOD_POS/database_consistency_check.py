def check_code_consistency():
    """‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≠‡∏î‡∏Ñ‡∏•‡πâ‡∏≠‡∏á‡πÉ‡∏ô‡πÇ‡∏Ñ‡πâ‡∏î"""
    print("\n=== ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≠‡∏î‡∏Ñ‡∏•‡πâ‡∏≠‡∏á‡πÉ‡∏ô‡πÇ‡∏Ñ‡πâ‡∏î ===")
    
    # ‡πÑ‡∏ü‡∏•‡πå‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö
    files_to_check = [
        'backend/app.py',
        'backend/database.py',
        'backend/admin.js'
    ]
    
    issues = []
    
    for file_path in files_to_check:
        if os.path.exists(file_path):
            print(f"\n‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÑ‡∏ü‡∏•‡πå: {file_path}")
            
            with open(file_path, 'r', encoding='utf-8') as f:
                content = f.read()
                
            # ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ special_request
            special_request_matches = re.findall(r'special_request', content)
            customer_request_matches = re.findall(r'customer_request', content)
            
            if special_request_matches:
                print(f"  ‚ö†Ô∏è  ‡∏û‡∏ö 'special_request' {len(special_request_matches)} ‡∏Ñ‡∏£‡∏±‡πâ‡∏á")
                issues.append(f"{file_path}: ‡∏û‡∏ö special_request {len(special_request_matches)} ‡∏Ñ‡∏£‡∏±‡πâ‡∏á")
            
            if customer_request_matches:
                print(f"  ‚úÖ ‡∏û‡∏ö 'customer_request' {len(customer_request_matches)} ‡∏Ñ‡∏£‡∏±‡πâ‡∏á")
        else:
            print(f"  ‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡πÑ‡∏ü‡∏•‡πå: {file_path}")
    
    return issues

